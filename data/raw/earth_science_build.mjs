const expectedUnits = ['geology', 'weather', 'ocean', 'astronomy', 'earth_system'];
if (units.length !== expectedUnits.length || units.some((unit, index) => unit.key !== expectedUnits[index])) {
  throw new Error(`單元不完整：預期 ${expectedUnits.join(', ')}`);
}

const allTerms = new Set();
const poolCounts = new Map();
let serial = 1;
const all = [];

for (const unit of units) {
  if (unit.items.length !== 36) throw new Error(`${unit.key} 應有 36 筆，目前 ${unit.items.length} 筆`);
  const rows = unit.items.map(([term, def, example, category, distractor_pool]) => {
    if (allTerms.has(term)) throw new Error(`term 重複：${term}`);
    allTerms.add(term);
    if (!example.includes(term)) throw new Error(`example 未包含 term：${term}`);
    if (example.includes(def)) throw new Error(`example 不得逐字複製 def：${term}`);
    poolCounts.set(distractor_pool, (poolCounts.get(distractor_pool) || 0) + 1);
    return {
      id: `d${String(serial++).padStart(4, '0')}`,
      term,
      unit: unit.key,
      grade: unit.grade,
      def,
      example,
      category,
      distractor_pool,
    };
  });
  all.push(...rows);
}

if (all.length !== 180) throw new Error(`總筆數應為 180，目前 ${all.length}`);
if (poolCounts.size !== 30) throw new Error(`誘答池應為 30，目前 ${poolCounts.size}`);
for (const [pool, count] of poolCounts) {
  if (count < 4) throw new Error(`誘答池 ${pool} 只有 ${count} 筆`);
}

let offset = 0;
for (const unit of units) {
  const rows = all.slice(offset, offset + unit.items.length);
  offset += unit.items.length;
  await writeFile(join(rawDir, `earth_science_${unit.key}.json`), `${JSON.stringify(rows, null, 2)}\n`);
}
await writeFile(join(dataDir, 'earth-science.json'), `${JSON.stringify(all, null, 2)}\n`);
console.log(`wrote ${all.length} entries across ${units.length} shards and ${poolCounts.size} pools`);
